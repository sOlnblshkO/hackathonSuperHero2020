package com.example.forfedorova.CustomClasses;

public class ActivityUnit {
    public String name, desc, adminLogin, preLogin, startDate, endDate;
    public int id;

    public ActivityUnit(String name, String desc, int id, String adminLogin, String preLogin, String startDate, String endDate) {
        this.name = name;
        this.desc = desc;
        this.id = id;
        this.adminLogin = adminLogin;
        this.preLogin = preLogin;
        this.startDate = startDate;
        this.endDate = endDate;
    }


}
